def get_user_agent() -> str:
    """
    Get a random user agent for web scraping
    """
    return random.choice(USER_AGENTS)


def get_desktop_user_agent() -> str:
    """Get a desktop-only user agent"""
    desktop_agents = [ua for ua in USER_AGENTS if "Windows" in ua or "Macintosh" in ua]
    return random.choice(desktop_agents) if desktop_agents else USER_AGENTS[0]


def get_mobile_user_agent() -> str:
    """Get a mobile-only user agent"""
    mobile_agents = [ua for ua in USER_AGENTS if "Android" in ua or "iPhone" in ua]
    return random.choice(mobile_agents) if mobile_agents else USER_AGENTS[0]


def get_chrome_user_agent() -> str:
    """Get a Chrome browser user agent"""
    chrome_agents = [ua for ua in USER_AGENTS if "Chrome" in ua and "Edg" not in ua]
    return random.choice(chrome_agents) if chrome_agents else USER_AGENTS[0]


def get_firefox_user_agent() -> str:
    """Get a Firefox browser user agent"""
    firefox_agents = [ua for ua in USER_AGENTS if "Firefox" in ua]
    return random.choice(firefox_agents) if firefox_agents else USER_AGENTS[0]